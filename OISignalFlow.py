def classify_signal(price_chg, oi_chg):
    """Classify OI buildup type"""
    if price_chg > 0 and oi_chg > 0:
        return "ūüüĘ LONG BUILDUP"      # ‚úÖ Best ‚ÄĒ Buy CE
    elif price_chg < 0 and oi_chg > 0:
        return "ūüĒī SHORT BUILDUP"     # ‚ĚĆ Avoid CE ‚ÄĒ Buy PE
    elif price_chg > 0 and oi_chg < 0:
        return "ūüü° SHORT COVERING"    # ‚ö†ÔłŹ Weak ‚ÄĒ Risky CE
    else:
        return "‚ö™ LONG UNWINDING"    # ‚ĚĆ Avoid all


