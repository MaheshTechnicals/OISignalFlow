def is_market_open():
    """Returns True only during NSE market hours"""
    now = datetime.now()
    h, m = now.hour, now.minute

    # Skip weekends
    if now.weekday() >= 5:
        return False
    # Before 9:15 AM
    if h < 9 or (h == 9 and m < 15):
        return False
    # After 3:30 PM
    if h > 15 or (h == 15 and m > 30):
        return False

    return True


